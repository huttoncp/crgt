#' "crgt: data processing and analysis for the cued Rat Gambling Task."
#'
#' "This package provides functions for processing and analysis of operant
#' behavioural data for the cued and uncued versions of the Rat Gambling Task
#' (RGT) and the cRGT training version of the five choice serial reaction time
#' task (5CSRT). Provided functions enable the user to parse raw MedPC data
#' files, pivot them between long and wide formats, enabling them to perform
#' common statistical analyses for these behavioural tasks as used in the
#' Winstanley laboratory at the University of British Columbia. This package
#' aims to facilitate reproducible and standardized analysis of RGT data. Note
#' This package is currently in development and its functionality will be
#' improved over time."
#'
